CREATE SCHEMA IF NOT EXISTS public;

-- Extension nécessaire pour la génération d'UUID si souhaité à la place des identifiants ERP textuels
CREATE EXTENSION IF NOT EXISTS "uuid-ossp";

-- Fonction utilitaire d'automatisation des dates de mise à jour (updated_at)
CREATE OR REPLACE FUNCTION public.update_modified_column()
RETURNS TRIGGER AS $$
BEGIN
    NEW.updated_at = now();
    RETURN NEW;
END;
$$ language plpgsql;

-- -------------------------------------------------------------------------------------
-- EN CLAVE 1. GESTION DES UTILISATEURS DU SYSTÈME ERP
-- -------------------------------------------------------------------------------------
CREATE TABLE IF NOT EXISTS public.users (
    id UUID PRIMARY KEY DEFAULT uuid_generate_v4(),
    email VARCHAR(255) UNIQUE NOT NULL,
    password_hash VARCHAR(255) NOT NULL, -- Stockage sécurisé des hashs
    role VARCHAR(50) NOT NULL CHECK (role IN ('admin', 'stocks', 'technical', 'commercial', 'manager')),
    name VARCHAR(255) NOT NULL,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL,
    updated_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL
);

-- Index pour accélérer les requêtes d'authentification
CREATE INDEX IF NOT EXISTS idx_users_email ON public.users(email);

-- -------------------------------------------------------------------------------------
-- EN CLAVE 2. COMPOSANTE RH & PAIE (EMPLOYÉS, PAIES, PRÉSENCES)
-- -------------------------------------------------------------------------------------

-- Table des Employés
CREATE TABLE IF NOT EXISTS public.employees (
    id VARCHAR(50) PRIMARY KEY, -- ex: 'EMP-001'
    name VARCHAR(255) NOT NULL,
    role VARCHAR(255) NOT NULL,
    email VARCHAR(255),
    phone VARCHAR(50),
    salary NUMERIC(10, 2) NOT NULL DEFAULT 0.00, -- Salaire mensuel d'embauche en USD
    status VARCHAR(50) NOT NULL DEFAULT 'Présent' CHECK (status IN ('Présent', 'Absent', 'En Mission', 'Congé')),
    photo_url TEXT,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL,
    updated_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL
);

-- Historique des versements salariaux (Aperçu des fiches de paie générées)
CREATE TABLE IF NOT EXISTS public.employee_salaries (
    id VARCHAR(50) PRIMARY KEY, -- ex: 'SLP-XXXXX' ou UUID
    employee_id VARCHAR(50) REFERENCES public.employees(id) ON DELETE CASCADE,
    payment_date DATE NOT NULL,
    amount NUMERIC(10, 2) NOT NULL, -- Montant versé en USD
    payment_status VARCHAR(50) NOT NULL DEFAULT 'Payé' CHECK (payment_status IN ('Payé', 'En Attente', 'Annulé')),
    created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL
);

-- Pointage journalier et feuille d'émargement (Attendance)
CREATE TABLE IF NOT EXISTS public.employee_attendance (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    employee_id VARCHAR(50) REFERENCES public.employees(id) ON DELETE CASCADE,
    attendance_date DATE NOT NULL,
    status VARCHAR(50) DEFAULT 'Présent' CHECK (status IN ('Présent', 'Absent', 'Demi-Journée')),
    CONSTRAINT unique_attendance_per_day UNIQUE (employee_id, attendance_date)
);

CREATE INDEX IF NOT EXISTS idx_attendance_date ON public.employee_attendance(attendance_date);
CREATE INDEX IF NOT EXISTS idx_salaries_emp ON public.employee_salaries(employee_id);

-- -------------------------------------------------------------------------------------
-- EN CLAVE 3. GESTION DU PARC TECHNIQUE & MACHINES
-- -------------------------------------------------------------------------------------

-- Table du Parc d'Équipements et Machinerie Interne
CREATE TABLE IF NOT EXISTS public.machinery (
    id VARCHAR(50) PRIMARY KEY, -- ex: 'MAC-001'
    name VARCHAR(255) NOT NULL,
    category VARCHAR(150) NOT NULL, -- ex: 'Agricole', 'Transformation Usine', 'Logistique'
    operating_hours INTEGER DEFAULT 0 NOT NULL,
    status VARCHAR(50) NOT NULL DEFAULT 'Opérationnel' CHECK (status IN ('Opérationnel', 'En Maintenance', 'En Panne', 'Hors Service')),
    photo_url TEXT,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL,
    updated_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL
);

-- Historique détaillé des maintenances sur les machines
CREATE TABLE IF NOT EXISTS public.machinery_maintenance (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    machinery_id VARCHAR(50) REFERENCES public.machinery(id) ON DELETE CASCADE NOT NULL,
    maintenance_date DATE NOT NULL,
    maintenance_type VARCHAR(100) NOT NULL CHECK (maintenance_type IN ('Régulière', 'Corrective', 'Préventive')),
    cost NUMERIC(10, 2) NOT NULL DEFAULT 0.00, -- Coût de la réparation en USD
    description TEXT NOT NULL,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL
);

CREATE INDEX IF NOT EXISTS idx_maintenance_machine ON public.machinery_maintenance(machinery_id);

-- -------------------------------------------------------------------------------------
-- EN CLAVE 4. GESTION DES STOCKS & MATIÈRES PREMIÈRES (LOGISTIQUE)
-- -------------------------------------------------------------------------------------

-- Table des Matières Premières (Matières Brutes en direct des champs)
CREATE TABLE IF NOT EXISTS public.stocks_raw_materials (
    id VARCHAR(50) PRIMARY KEY, -- ex: 'RM-001'
    name VARCHAR(255) NOT NULL,
    tonnage NUMERIC(12, 3) NOT NULL DEFAULT 0.000, -- Stock disponible en tonnes
    unit VARCHAR(50) NOT NULL DEFAULT 'tonnes',
    created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL,
    updated_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL
);

-- Table des Produits Finis (Farine, Huile de canole/soja)
CREATE TABLE IF NOT EXISTS public.stocks_finished_products (
    id VARCHAR(50) PRIMARY KEY, -- ex: 'FP-001'
    name VARCHAR(255) NOT NULL,
    quantity INTEGER NOT NULL DEFAULT 0, -- Stock physique disponible (Sacs ou bouteilles)
    unit VARCHAR(100) NOT NULL, -- ex: 'sacs d''emballage 25kg', 'bouteilles 1L'
    created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL,
    updated_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL
);

-- Table des Consommables essentiels (Sacs vides de farine, Bouteilles vides, Engrais NPK, Pesticides)
CREATE TABLE IF NOT EXISTS public.stocks_consumables (
    id VARCHAR(50) PRIMARY KEY, -- ex: 'CO-001'
    name VARCHAR(255) NOT NULL,
    quantity NUMERIC(12, 2) NOT NULL DEFAULT 0.00, -- Pièces ou litres physiques
    unit VARCHAR(55) NOT NULL, -- 'pièces', 'litres', 'tonnes'
    warning_threshold NUMERIC(12, 2) NOT NULL DEFAULT 0.00, -- Seuil critique de déclenchement d'alerte
    created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL,
    updated_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL
);

-- Table de Configuration des Vitesses d'Usine & Transformation
CREATE TABLE IF NOT EXISTS public.production_rates (
    rate_key VARCHAR(100) PRIMARY KEY, -- ex: 'cornProcessingRate', 'soyProcessingRate'
    rate_value NUMERIC(10, 2) NOT NULL, -- Quantité requise/consommée par jour
    notes TEXT,
    updated_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL
);

-- -------------------------------------------------------------------------------------
-- EN CLAVE 5. DÉPARTEMENT COMMERCIAL (VENTES DE PRODUITS FINIS, ACHATS DE FOURNITURE)
-- -------------------------------------------------------------------------------------

-- Table des Ventes (Factures Clients)
CREATE TABLE IF NOT EXISTS public.sales (
    id VARCHAR(50) PRIMARY KEY, -- ex: 'FAC-001', 'FAC-002'
    client_name VARCHAR(255) NOT NULL,
    product_name VARCHAR(255) NOT NULL,
    quantity INTEGER NOT NULL CHECK (quantity > 0),
    amount NUMERIC(12, 2) NOT NULL CHECK (amount >= 0.00), -- Total facturé en USD
    sale_date DATE NOT NULL,
    status VARCHAR(50) NOT NULL DEFAULT 'En Attente' CHECK (status IN ('Payé', 'En Attente', 'Annulé')),
    created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL,
    updated_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL
);

-- Table des Commandes et Approvisionnements (Fournisseurs - ex: Sacs vides PlastKat)
CREATE TABLE IF NOT EXISTS public.purchases (
    id VARCHAR(50) PRIMARY KEY, -- ex: 'BON-001'
    supplier_name VARCHAR(255) NOT NULL,
    product_name VARCHAR(255) NOT NULL,
    quantity INTEGER NOT NULL CHECK (quantity > 0),
    amount NUMERIC(12, 2) NOT NULL CHECK (amount >= 0.00), -- Coût d'achat total en USD
    purchase_date DATE NOT NULL,
    status VARCHAR(50) NOT NULL DEFAULT 'Planifié' CHECK (status IN ('Reçu', 'En Cours', 'Planifié', 'Annulé')),
    delivery_date DATE, -- Date de livraison estimée
    created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL,
    updated_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL
);

-- -------------------------------------------------------------------------------------
-- EN CLAVE 6. SYSTÈME DE SUPERVISION AUTOMATIQUE (ALERTES LOGISTIQUES ET OPÉRATIONNELLES)
-- -------------------------------------------------------------------------------------
CREATE TABLE IF NOT EXISTS public.alerts (
    id VARCHAR(50) PRIMARY KEY, -- ex: 'ALT-001'
    timestamp TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL,
    product_name VARCHAR(255) NOT NULL,
    current_level NUMERIC(12, 2) NOT NULL,
    seuil_level NUMERIC(12, 2) NOT NULL,
    alert_text TEXT NOT NULL,
    status VARCHAR(50) NOT NULL DEFAULT 'unread' CHECK (status IN ('unread', 'read', 'archived')),
    created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL
);

CREATE INDEX IF NOT EXISTS idx_alerts_status ON public.alerts(status);

